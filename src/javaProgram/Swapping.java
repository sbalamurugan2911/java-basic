package javaProgram;

public class Swapping {

	public static void main(String[] args) {
		int a=-2,b=0,c=10,d;
		
		System.out.println("Before Swapping");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		d=a;
		a=b;
		b=c;
		c=d;
		System.out.println("After Swapping");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
}
}